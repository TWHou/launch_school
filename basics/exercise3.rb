# Write a program that uses a hash to store a list of movie titles 
# with the year they came out. Then use the puts command to make 
# your program print out the year of each movie to the screen. 

movies = { Gone_with_the_Wind: 1939, 
           Avatar: 2009, 
           Star_Wars: 1977, 
           Titanic: 1997, 
           The_Sound_of_Music: 1965 }

puts movies[:Gone_with_the_Wind]
puts movies[:Avatar]
puts movies[:Star_Wars]
puts movies[:Titanic]
puts movies[:The_Sound_of_Music]
